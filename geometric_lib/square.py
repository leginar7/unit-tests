def area(a):
    '''
    Принимает сторону квадрата a (float или int).
    Возвращает площадь квадрата (float),
    вычисляемую по формуле: a².
    '''
    return a * a


def perimeter(a):
    '''
    Принимает сторону квадрата a (float или int).
    Возвращает периметр квадрата (float),
    вычисляемый по формуле: 4 * a.
    '''
    return 4 * a
